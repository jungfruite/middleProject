package spectrum.sale.vo;

public class SaleVO {
	
	 private String paymentNumber;
	 private String memberId;
	 private String paymentDate;
	 private String paymentAmount;
	 private String paymentRefundyn;
	 private String paymentTotalamount;
	 
	 
	public String getPaymentNumber() {
		return paymentNumber;
	}
	public void setPaymentNumber(String paymentNumber) {
		this.paymentNumber = paymentNumber;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getPaymentDate() {
		return paymentDate;
	}
	public void setPaymentDate(String paymentDate) {
		this.paymentDate = paymentDate;
	}
	public String getPaymentAmount() {
		return paymentAmount;
	}
	public void setPaymentAmount(String paymentAmount) {
		this.paymentAmount = paymentAmount;
	}
	public String getPaymentRefundyn() {
		return paymentRefundyn;
	}
	public void setPaymentRefundyn(String paymentRefundyn) {
		this.paymentRefundyn = paymentRefundyn;
	}
	public String getPaymentTotalamount() {
		return paymentTotalamount;
	}
	public void setPaymentTotalamount(String paymentTotalamount) {
		this.paymentTotalamount = paymentTotalamount;
	}
	
	 
	 
}
